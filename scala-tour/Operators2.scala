// Infix operators

object Operators2 {
    def main(args: Array[String]): Unit = {
            println(1 + 2)
            println(1.+(2))
            val x = "abc def ijk".split(" ")    
            val y = "abc def ijk" split " "

            for (item <- x) {
                println(item)
            }

            for (item <- y) {
                println(item)
            }

    }
}		
